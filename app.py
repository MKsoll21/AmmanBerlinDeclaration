def create_dashboard(
    Recipient="All",
    Donor="All",
    Sector="All"
):

    filtered = data.copy()


    if Recipient != "All":
        filtered = filtered[
            filtered["Recipient"] == Recipient
        ]


    if Donor != "All":
        filtered = filtered[
            filtered["Donor"] == Donor
        ]


    if Sector != "All":
        filtered = filtered[
            filtered["Sector"] == Sector
        ]


    result = (
        filtered["Disability Category"]
        .value_counts()
        .reset_index()
    )


    result.columns = [
        "Category",
        "Count"
    ]


    result["Percentage"] = (
        result["Count"]
        /
        result["Count"].sum()
        *
        100
    ).round(1)


    fig = px.bar(
        result,
        x="Category",
        y="Count",
        text=result.apply(
            lambda x:
            f'{x["Count"]}<br>{x["Percentage"]}%',
            axis=1
        ),
        color="Category",
        title="Amman-Berlin Declaration - Disability Inclusion Marker"
    )


    fig.update_layout(
        xaxis_title="Disability Inclusion Category",
        yaxis_title="Number of commitments"
    )


    fig.show()


    display(result)

