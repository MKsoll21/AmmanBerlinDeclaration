def disability_category(value):

    if pd.isna(value) or value == "":
        return "Not scored"

    try:
        value = int(value)
    except:
        return "Not scored"

    if value == 0:
        return "Scored - not targeted"

    if value in [1, 2]:
        return "Targeted"

    return "Not scored"


data["Disability Category"] = (
    data["DISABILITY"]
    .apply(disability_category)
)


